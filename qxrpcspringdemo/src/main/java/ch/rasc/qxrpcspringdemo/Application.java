package ch.rasc.qxrpcspringdemo;

import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.EnableAutoConfiguration;
import org.springframework.boot.builder.SpringApplicationBuilder;
import org.springframework.boot.context.web.SpringBootServletInitializer;
import org.springframework.context.annotation.ComponentScan;
import org.springframework.context.annotation.Configuration;
import org.springframework.scheduling.annotation.EnableScheduling;

import ch.rasc.qxrpcspring.QxrpcSpring;

@Configuration
@ComponentScan(basePackageClasses = { Application.class, QxrpcSpring.class })
@EnableAutoConfiguration
@EnableScheduling
public class Application extends SpringBootServletInitializer {

	// To activate development profile add the following line to the command line
	// -Dspring.profiles.active=development

	@Override
	protected SpringApplicationBuilder configure(SpringApplicationBuilder application) {
		return application.sources(Application.class);
	}

	public static void main(String[] args) throws Exception {
		SpringApplication.run(Application.class, args);
	}

}
