package ch.ess.tt.model;

public class Fall {

  private int id;
  private int nummer;
  private int testNummer;
  private String anweisung;
  private String ereigniss;
  private String navigation;

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public int getNummer() {
    return nummer;
  }

  public void setNummer(int nummer) {
    this.nummer = nummer;
  }

  public int getTestNummer() {
    return testNummer;
  }

  public void setTestNummer(int testNummer) {
    this.testNummer = testNummer;
  }

  public String getAnweisung() {
    return anweisung;
  }

  public void setAnweisung(String anweisung) {
    this.anweisung = anweisung;
  }

  public String getEreigniss() {
    return ereigniss;
  }

  public void setEreigniss(String ereigniss) {
    this.ereigniss = ereigniss;
  }

  public String getNavigation() {
    return navigation;
  }

  public void setNavigation(String navigation) {
    this.navigation = navigation;
  }

}
