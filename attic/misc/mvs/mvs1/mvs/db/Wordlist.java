



package mvs.db;

import com.objectmatter.bsf.*;
import java.util.*;
import java.math.*;

/**
 * Class Wordlist.
 */
public class Wordlist {

    /* attributes */
    private long hash;
    private String word;
    

    /** Creates a new Wordlist */    
    public Wordlist() {
    }
    
    
    /** Accessor for attribute hash */
    public long getHash() {
        return hash;
    }
    
    /** Accessor for attribute word */
    public String getWord() {
        return word;
    }
    
    
    /** Mutator for attribute hash */
    public void setHash(long newHash) {
        hash = newHash;
    }
    
    /** Mutator for attribute word */
    public void setWord(String newWord) {
        word = newWord;
    }
    
    
    
    
    
    
    
    
    
    
}



